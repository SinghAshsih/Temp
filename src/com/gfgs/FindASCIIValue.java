package com.gfgs;

public class FindASCIIValue {
	public static void main(String[] args) {
		char ch = 'A';
		char ch2 = 'a';
		char ch3 = 'Z';
		char ch4 = 'z';
		int i = ch;
		int i2 = ch2;
		int i3 = ch3;
		int i4 = ch4;
		System.out.println("ASCII value of " + ch + " is : " + i);
		System.out.println("ASCII value of " + ch2 + " is : " + i2);
		System.out.println("ASCII value of " + ch3 + " is : " + i3);
		System.out.println("ASCII value of " + ch4 + " is : " + i4);

	}
}
